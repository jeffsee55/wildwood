import { access, cp, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";


const wildwoodRoot = path.join(path.dirname(fileURLToPath(import.meta.url)), "..");
const extensionRoot = path.join(wildwoodRoot, "..", "extension");
const dest = path.join(wildwoodRoot, "bundled-extension");
const genTs = path.join(
  wildwoodRoot,
  "src/nextjs/bundled-extension-bytes.gen.ts",
);

const required = ["dist/extension.js", "package.json", "themes/wildwood-dark.json"];

async function assertExtensionBuilt() {
  for (const rel of required) {
    try {
      await access(path.join(extensionRoot, rel));
    } catch {
      throw new Error(
        `wildwood-vscode is missing ${rel}. Run pnpm --filter wildwood-vscode build before wildwood build.`,
      );
    }
  }
}

await assertExtensionBuilt();

await cp(path.join(extensionRoot, "dist"), path.join(dest, "dist"), {
  recursive: true,
});
await cp(path.join(extensionRoot, "themes"), path.join(dest, "themes"), {
  recursive: true,
});
for (const file of ["package.json", "package.nls.json"]) {
  await cp(path.join(extensionRoot, file), path.join(dest, file));
}

const extensionJs = await readFile(path.join(extensionRoot, "dist/extension.js"));
const extensionJsBase64 = extensionJs.toString("base64");
const themeJson = await readFile(
  path.join(extensionRoot, "themes/wildwood-dark.json"),
);
const themeJsonBase64 = themeJson.toString("base64");

const pkgJsonRaw = await readFile(path.join(extensionRoot, "package.json"), "utf8");
const pkgJson = JSON.parse(pkgJsonRaw);
const pkgMinimal = {
  name: pkgJson.name ?? "wildwood-vscode",
  publisher: pkgJson.publisher ?? "jsee",
  version: pkgJson.version ?? "0.0.0",
  enabledApiProposals: Array.isArray(pkgJson.enabledApiProposals) ? pkgJson.enabledApiProposals : [],
};
const pkgJsonBase64 = Buffer.from(JSON.stringify(pkgMinimal), "utf8").toString("base64");

let nlsJsonBase64 = "";
try {
  const nlsRaw = await readFile(path.join(extensionRoot, "package.nls.json"), "utf8");
  nlsJsonBase64 = Buffer.from(nlsRaw, "utf8").toString("base64");
} catch {
  nlsJsonBase64 = "";
}

await writeFile(
  genTs,
  `/** Generated by scripts/copy-bundled-extension.mjs — do not edit. */
const EXTENSION_JS_BASE64 = ${JSON.stringify(extensionJsBase64)};
const WILDWOOD_DARK_THEME_JSON_BASE64 = ${JSON.stringify(themeJsonBase64)};
const EXTENSION_PKG_JSON_BASE64 = ${JSON.stringify(pkgJsonBase64)};
const EXTENSION_NLS_JSON_BASE64 = ${JSON.stringify(nlsJsonBase64)};

export function getExtensionJsBytes(): Uint8Array {
  return new Uint8Array(Buffer.from(EXTENSION_JS_BASE64, "base64"));
}

export function getWildwoodDarkThemeBytes(): Uint8Array {
  return new Uint8Array(Buffer.from(WILDWOOD_DARK_THEME_JSON_BASE64, "base64"));
}

export function getExtensionPackageJson(): { name: string; publisher: string; version: string; enabledApiProposals: string[] } {
  return JSON.parse(Buffer.from(EXTENSION_PKG_JSON_BASE64, "base64").toString("utf8"));
}

export function getExtensionNlsJson(): unknown {
  if (!EXTENSION_NLS_JSON_BASE64) return {};
  try {
    return JSON.parse(Buffer.from(EXTENSION_NLS_JSON_BASE64, "base64").toString("utf8"));
  } catch {
    return {};
  }
}

export function getExtensionPackageJsonBytes(): Uint8Array {
  return new Uint8Array(Buffer.from(EXTENSION_PKG_JSON_BASE64, "base64"));
}

export function getExtensionNlsJsonBytes(): Uint8Array | null {
  if (!EXTENSION_NLS_JSON_BASE64) return null;
  return new Uint8Array(Buffer.from(EXTENSION_NLS_JSON_BASE64, "base64"));
}
`,
  "utf8",
);

console.info(
  "[wildwood] copied wildwood-vscode into bundled-extension/ (+ embedded extension.js + theme)",
);

